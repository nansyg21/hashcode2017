public class Cache {


    int id;
    int size;

    public Cache(int id, int size) {
        this.id = id;
        this.size = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}
    
}